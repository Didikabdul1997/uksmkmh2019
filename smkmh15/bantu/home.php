                                            <p> Kesehatan Masyarakat (Puskesmas) adalah salah satu sarana pelayanan kesehatan masyarakat yang amat penting di Indonesia. Puskesmas adalah unit pelaksana teknis dinas kabupaten/kota yang bertanggungjawab menyelenggarakan pembangunan kesehatan di suatau wilayah kerja (Depkes, 2011).
                                            Pengertian puskesmas adalah suatu unit pelaksana fungsional yang berfungsi sebagai pusat pembangunan kesehatan, pusat pembinaan peran serta masyarakat dalam bidang kesehatan serta pusat pelayanan kesehatan tingkat pertama yang menyelenggarakan kegiatannya secara menyeluruh, terpadu yang berkesinambungan pada suatu masyarakat yang bertempat tinggal dalarn suatu wilayah tertentu (Azrul Azwar, 1996).</p>
                                            <p>Puskesmas merupakan kesatuan organisasi fungsional yang menyelenggarakan upaya kesehatan yang bersifat menyeluruh, terpadu, merata dapat diterima dan terjangkau oleh masyarakat dengan peran serta aktif masyarakat dan menggunakan hasil pengembangan ilmu pengetahuan dan teknologi tepat guna, dengan biaya yang dapat dipikul oleh pemerintah dan masyarakat luas guna mencapai derajat kesehatan yang optimal, tanpa mengabaikan mutu pelayanan kepada perorangan (Depkes, 2009).</p>
                                            <p>Visi dan Misi Puskesmas 
                                            Visi puskesmas adalah tercapainya kecamatan sehat menuju Indonesia sehat. Indikator utama yakni:<br/>
                                            1.  Lingkungan sehat. <br/>
                                            2.  Perilaku sehat.<br/>
                                            3.  Cakupan pelayanan kesehatan yang bermutu.<br/>
                                            4.  Derajat kesehatan penduduk kecamatan.<br/>

                                            Misi puskesmas, yaitu:<br/>
                                            1.  Menggerakkan pembangunan berwawasan kesehatan di wilayah kerjanya.<br/> 
                                            2.  Mendorong kemandirian hidup sehat bagi keluarga dan masyarakat di wilayah kerjanya.<br/>
                                            3.  Memelihara dan meningkatkan mutu, pemerataan dan keterjangkauan pelayanan kesehatan yang diselenggarakan.<br/> 
                                            4.  Memelihara dan meningkatkan kesehatan perorangan, keluarga dan masyarakat berserta lingkungannya..</p>
