                                            <h1><a class="blog-ht" href="#">Tentang</a></h1>
                                            <p>Ada yang tahu sejak kapan lomba burung kicauan mulai digelar di Indonesia. Burung apa saja yang dilombakan saat itu? Saya sendiri semula tidak tahu, sampai kemudian menemukan artikel menarik yang ditulis Dr Paul Jepson, ilmuwan dari Pusat Lingkungan Universitas Oxford Inggris, yang dimuat di BirdAsia 9 (2008), halaman 58-60. Artikel ini sebenarnya menyoroti anis merah, tetapi ada beberapa yang menceritakan sejarah lomba burung kicauan, dan perlu di-share kepada sobat kicaumania..</p>
                                            <p>Dalam artikel berjudul “Orange-headed thrush/Zoothera citrina and the avian X-factor”, Paul Jepson dengan fasih menyebut penggemar burung kicauan di Indonesia sebagai kicaumania.

“This is the world of kicau-mania, a way of enjoying birds quite different from the western focus on bird finding and identification, scarcity and counting,” tulis Jepson dalam artikel tersebut..</p>
<p>
Ya, menurut Jepson, kicaumania memiliki cara menikmati (kicauan) burung yang sangat berbeda dari Barat yang hanya fokus ke pencarian dan identifikasi burung, menentukan apakah ini burung langka atau tidak, lalu menghitung populasinya.
</p><p>
Kicaumania Indonesia justru lebih memperhatikan estetika lagu yang dikeluarkan burung, bentuk atau postur tubuhnya (maksudnya katuranggan), menurunkannya dalam lomba burung untuk dinilai juri, atau dijadikan burung penyanyi di rumah.
</p><p>
Sejak ratusan tahun lalu, burung yang popular di Indonesia terutama dalam masyarakat Jawa adalah perkutut. Filosofi Jawa yang dianut leluhur zaman dulu menegaskan, seorang pria harus mempunyai garwa, wisma, turangga, curiga, dankukila.
</p><p>
Garwa adalah istri, dan wisma berarti rumah. Turangga berarti kuda, yang bisa ditafsirkan sebagai kendaraan (mobil, sepeda motor, sepeda). Adapun curiga atau keris berarti senjata, yang difahami sebagian orang sebagai pekerjaan atau punya penghasilan.
</p><p>
Nah, kukila berarti burung. Di masa lalu, pengertian kukila lebih didominasi oleh perkutut. Sekarang sudah mulai melebar bukan hanya perkutut, melainkan juga burung kicauan, hewan piaraan, bahkan kukila bisa ditafsirkan ke hobi apapun (moge, koleksi barang antik, dll).</p>
