                                            <h1><a class="blog-ht" href="#"></p>Welcome To My Coperative Application</a></h1>
                                            <p>The cooperative in Indonesia was first established in 1896 by a Mayor in Purwokerto (Nasution, 1990). The establishment of a cooperative at that time  was to protect the citizens from being trapped in loan sharks. Type  of established  cooperative  was  saving and  loan  cooperatives.  This business  entity  developed into  the agricultural  sector  cooperatives.  Then  in  1908  and  1911,  Boedi  Oetomo  and  Syarekat  Islam  advocated establishing a cooperative as a means of economic struggle at the time. After Indonesia gained its independence in 1945, the cooperative was explicitly incorporated as the basis of a nation-based economy based on article 33 paragraph one of the 1945 Constitution</p>
                                            <p>Until 2016, the number of cooperatives in Indonesia reached 212,135 units with a total membership of 37,783,160 people (Ministry of Cooperatives and SMEs, 2016). Of those cooperatives, 30%  were inactive for various reasons. There are several factors that  trigger the  cooperatives were inactive, such  as the  low human resources of cooperatives, cooperative management was not professional, cooperative members' participation was still lacking and mastery of the technology was inadequate. Besides, there were some cases of cooperatives that  did  deviations  in  managing  cooperatives,  so  that  the  distrust  of  the  community  to  cooperatives  was increasing.</p>
                                            <p>Looking at the releases issued by the International Cooperative Alliance (ICA) in www.monitor.coop, many cooperatives in the United States fill the list.  300 major cooperatives in the world,  the first rank  was a cooperative  Zenykoren from  Japan  that engaged  in the insurance  sector. Followed  by Acdlec  Electry Coop France, in the trade and services sector, and ranked third was the State Farm United States that engaged in the insurance  sector. The  cooperative of  Indonesia,  Gresik cement  co-operatives  were ranked  232  in 2013  and ranked 210 in 2012. Meanwhile, according to the Minister of Cooperatives and SMEs, Gede Puspayoga (2016), Indonesia managed to penetrate the world's cooperative, the Telkomsel Cooperative (Kisel) which was ranked 123  out of  300 world  cooperatives.  Of the  300 best  cooperatives in  the world,  Indonesia has only  one co-operative, while the United States has the 100 best cooperatives from the top 300 cooperatives in the world.</p>
                                            <p>Based on the  above background,  this study aims to evaluate  the contribution of  cooperatives to  the Indonesian economy. Cooperatives also contribute to reducing poverty, unemployment and improving people's welfare (DeVille et al., 2007). In addition, cooperatives in Indonesia are able to absorb labor by 0.5%  of the total working population (Ministry of Cooperatives and SMEs, 2016). To achieve the purpose of this study, this  study used descriptive analysis.  This study  focuses on the existing data on cooperatives, such as capital, turnover, profit, and cooperative labor. The  rest of  the study  is organized  in  the following  sections.  Section 2  provides  the  literature and discussion of the results, and section 3 concludes the paper.  </p>
